<script>
document.addEventListener('DOMContentLoaded', function () {
    var lightbox = document.getElementById('epgLightbox');
    if (!lightbox) return;

    var lightboxImg = lightbox.querySelector('.epg-lightbox-img');
    var closeBtn = lightbox.querySelector('.epg-lightbox-close');
    var backdrop = lightbox.querySelector('.epg-lightbox-backdrop');

    function openLightbox(src, alt) {
        lightboxImg.src = src;
        lightboxImg.alt = alt || '';
        lightbox.classList.add('open');
        lightbox.setAttribute('aria-hidden', 'false');
        document.body.style.overflow = 'hidden';
    }

    function closeLightbox() {
        lightbox.classList.remove('open');
        lightbox.setAttribute('aria-hidden', 'true');
        document.body.style.overflow = '';
    }

    document.querySelectorAll('.epg-brochure-item img').forEach(function (img) {
        img.addEventListener('click', function () {
            openLightbox(img.src, img.alt);
        });
    });

    closeBtn.addEventListener('click', closeLightbox);
    backdrop.addEventListener('click', closeLightbox);

    document.addEventListener('keydown', function (e) {
        if (e.key === 'Escape' || e.key === 'Esc') {
            if (lightbox.classList.contains('open')) {
                closeLightbox();
            }
        }
    });
});
</script>

